class Player {
  String id;
  String nickname;
  String fullName;
  String contactNumber;
  String email;
  String address;
  String remarks;
  String level;

  Player({
    required this.id,
    required this.nickname,
    required this.fullName,
    required this.contactNumber,
    required this.email,
    required this.address,
    required this.remarks,
    required this.level,
  });
}
